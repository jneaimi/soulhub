#!/usr/bin/env node
/**
 * Stamp the current git HEAD SHA + ISO timestamp into src/lib/build-info.ts.
 * Called as `prebuild`, `predev`, and at the start of `npm run check` so
 * Vite / svelte-check always have a fresh value to inline.
 *
 * ADR-016: If the working dir is not a git checkout (or git is unavailable),
 * writes BUILD_SHA = 'unknown' so the deploy-pending detector is gracefully
 * disabled (never false-positives). Zero perpetual git drift — the file is
 * gitignored; it only lives in the working tree during an active build/dev
 * session.
 */
import { writeFileSync } from 'node:fs';
import { execFileSync } from 'node:child_process';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = resolve(ROOT, 'src', 'lib', 'build-info.ts');

let sha = 'unknown';
const buildTime = new Date().toISOString();

try {
  sha = execFileSync('git', ['rev-parse', 'HEAD'], {
    cwd: ROOT,
    encoding: 'utf-8',
    stdio: ['ignore', 'pipe', 'ignore'],
    timeout: 3000,
  }).trim();
  // Basic sanity: a git SHA is 40 hex chars.
  if (!/^[0-9a-f]{40}$/.test(sha)) sha = 'unknown';
} catch {
  // Not a git checkout or git unavailable — deploy detector will be disabled.
}

writeFileSync(
  OUT,
  `// AUTO-GENERATED by scripts/stamp-build-info.mjs — do not edit manually.
// File is gitignored; regenerated on prebuild / predev / npm run check.
// BUILD_SHA = 'unknown' disables the deploy-pending detector (ADR-016).
export const BUILD_SHA = '${sha}';
export const BUILD_TIME = '${buildTime}';
`,
);
